#!/usr/bin/env node
/**
 * Full Dev Pipeline Validator Hook
 *
 * SubagentStop hook that validates gate conditions for the full-dev-pipeline skill.
 * Runs after each subagent completes to enforce:
 * - Proposal gate: All 4 reviewers must approve before proceeding
 * - Review gate: All 3 reviewers (Opus, Codex, Gemini) must approve
 * - PR finalization: All bot comments resolved, CI green
 * - Commit conventions: No co-author tags
 *
 * Hook reads the current pipeline state from .task/pipeline-state.json
 * and validates based on the current stage.
 */

const fs = require('fs');
const path = require('path');

const TASK_DIR = '.task';
const STATE_FILE = path.join(TASK_DIR, 'pipeline-state.json');

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch {
    return null;
  }
}

function getState() {
  return readJson(STATE_FILE);
}

function validateProposalGate(state) {
  const errors = [];
  const proposalDir = path.join(TASK_DIR, 'proposal');

  // Check all 4 reviews exist and are approved
  const required = ['review-opus.json', 'review-sonnet.json', 'review-codex-medium.json', 'review-codex-high.json'];

  for (const file of required) {
    const review = readJson(path.join(proposalDir, file));
    if (!review) {
      errors.push(`Missing proposal review: ${file}`);
      continue;
    }
    if (review.status !== 'approved') {
      errors.push(`Proposal review ${file} not approved (status: ${review.status})`);
    }
  }

  // Check user approval
  const userApproval = readJson(path.join(proposalDir, 'user-approval.json'));
  if (!userApproval || !userApproval.approved) {
    errors.push('User has not approved the proposal');
  }

  return errors;
}

function validateReviewGate(state) {
  const errors = [];
  const reviewDir = path.join(TASK_DIR, 'reviews');

  // Check all 3 reviews exist and are approved
  const required = ['review-opus-security.json', 'review-codex-high.json', 'review-gemini.json'];

  for (const file of required) {
    const review = readJson(path.join(reviewDir, file));
    if (!review) {
      errors.push(`Missing code review: ${file}`);
      continue;
    }
    if (review.status !== 'approved') {
      errors.push(`Code review ${file} not approved (status: ${review.status})`);
    }
  }

  // Check that all reviewers have argued and reached consensus
  const consensus = readJson(path.join(reviewDir, 'consensus.json'));
  if (!consensus || !consensus.fully_agreed) {
    errors.push('Reviewers have not reached full consensus');
  }

  return errors;
}

function validateCommitConventions() {
  const errors = [];

  // Check recent commits for co-author tags
  try {
    const { execSync } = require('child_process');
    const log = execSync('git log --format="%B" -5', { encoding: 'utf8' });

    if (/co-authored-by.*claude/i.test(log)) {
      errors.push('Found Co-Authored-By tag mentioning Claude in recent commits');
    }
    if (/generated.*claude/i.test(log) || /claude.*generated/i.test(log)) {
      errors.push('Found "generated by Claude" text in recent commits');
    }
  } catch {
    // Can't check, skip
  }

  return errors;
}

function validateCIStatus() {
  const errors = [];
  const prStatus = readJson(path.join(TASK_DIR, 'pr-status.json'));

  if (!prStatus) {
    errors.push('No PR status data available');
    return errors;
  }

  if (!prStatus.ci?.all_green) {
    errors.push(`CI is not green (state: ${prStatus.ci?.state})`);
    if (prStatus.ci?.failed?.length > 0) {
      prStatus.ci.failed.forEach((check) => {
        errors.push(`  Failed check: ${check.name}`);
      });
    }
  }

  if (prStatus.review_threads?.unresolved > 0) {
    errors.push(`${prStatus.review_threads.unresolved} unresolved review threads -- ALL must be resolved before completion`);
    if (prStatus.review_threads.unresolved_details?.length > 0) {
      prStatus.review_threads.unresolved_details.forEach((thread) => {
        const preview = thread.first_comment?.slice(0, 100) || 'no preview';
        errors.push(`  Unresolved thread by ${thread.author}: ${preview}`);
      });
    }
  }

  // Check for unresolved bot issues specifically
  if (prStatus.unresolved_bot_issues?.length > 0) {
    errors.push(`${prStatus.unresolved_bot_issues.length} unresolved bot comments -- reply and resolve each one`);
    prStatus.unresolved_bot_issues.forEach((issue) => {
      const preview = issue.body?.slice(0, 100) || 'no preview';
      errors.push(`  ${issue.bot} bot: ${preview}`);
    });
  }

  return errors;
}

function validatePRDescription() {
  const errors = [];

  try {
    const { execSync } = require('child_process');
    // Get PR body if we know the PR number
    const prStatus = readJson(path.join(TASK_DIR, 'pr-status.json'));
    if (prStatus?.pr_number) {
      const prBody = execSync(
        `gh pr view ${prStatus.pr_number} --json body -q .body`,
        { encoding: 'utf8' }
      );
      if (/generated.*claude/i.test(prBody) || /claude.*code/i.test(prBody)) {
        errors.push('PR description contains Claude auto-generated text');
      }
      if (prBody.includes('\u2014')) {
        errors.push('PR description contains em dash (\u2014), use informal language');
      }
    }
  } catch {
    // Can't check, skip
  }

  return errors;
}

function validateTDDCompliance() {
  const errors = [];

  // Check red phase artifact exists
  const redPhase = readJson(path.join(TASK_DIR, 'red-phase.json'));
  if (!redPhase) {
    errors.push('Missing .task/red-phase.json -- tests must be written BEFORE implementation');
    return errors; // No point checking further
  }

  if (redPhase.phase !== 'red') {
    errors.push('red-phase.json has wrong phase marker');
  }

  // Check green phase artifact exists
  const greenPhase = readJson(path.join(TASK_DIR, 'green-phase.json'));
  if (!greenPhase) {
    errors.push('Missing .task/green-phase.json -- implementation must make tests pass');
    return errors;
  }

  if (!greenPhase.existing_tests_passing) {
    errors.push('GREEN phase: existing tests are not passing (regressions introduced)');
  }

  if (!greenPhase.new_tests_passing) {
    errors.push('GREEN phase: new tests are not passing');
  }

  // Check impl-result for TDD compliance
  const implResult = readJson(path.join(TASK_DIR, 'impl-result.json'));
  if (!implResult) {
    errors.push('Missing .task/impl-result.json');
    return errors;
  }

  if (!implResult.tdd_compliance) {
    errors.push('impl-result.json missing tdd_compliance section');
    return errors;
  }

  const tdd = implResult.tdd_compliance;

  if (!tdd.red_phase_completed) {
    errors.push('TDD red phase was not completed');
  }
  if (!tdd.green_phase_completed) {
    errors.push('TDD green phase was not completed');
  }
  if (!tdd.tests_written_before_impl) {
    errors.push('Tests were NOT written before implementation -- TDD violation');
  }

  // For bug fixes: regression test is mandatory
  if (implResult.type === 'fix') {
    if (!tdd.regression_test_exists) {
      errors.push('Bug fix REQUIRES a regression test but none found');
    }

    // Verify regression test details exist
    const regressionTests = implResult.regression_tests || [];
    if (regressionTests.length === 0) {
      errors.push('Bug fix must have at least one entry in regression_tests array');
    }

    // Cross-check with red phase
    const redRegressions = (redPhase.regression_tests || []).filter((t) => t.confirms_bug === true);
    if (redRegressions.length === 0) {
      errors.push('RED phase has no regression test with confirms_bug: true -- the test must prove the bug exists before fixing');
    }
  }

  // For features: all ACs must be covered
  if (implResult.type === 'feat') {
    if (!tdd.all_acs_covered) {
      errors.push('Feature implementation: not all acceptance criteria have test coverage');
    }
  }

  // Check test results
  if (implResult.test_results) {
    if (implResult.test_results.failed > 0) {
      errors.push(`Test suite has ${implResult.test_results.failed} failing tests`);
    }
  }

  return errors;
}

// ================== MAIN ==================

function main() {
  const state = getState();

  // If no pipeline state, this hook is not relevant (different pipeline running)
  if (!state || state.pipeline !== 'full-dev-pipeline') {
    process.exit(0);
  }

  const stage = state.current_stage;
  let errors = [];

  switch (stage) {
    case 'proposal_review':
      errors = validateProposalGate(state);
      break;
    case 'tdd_validation':
    case 'implementation_green':
    case 'implementation_refactor':
      errors = validateTDDCompliance();
      break;
    case 'code_review':
      errors = [...validateTDDCompliance(), ...validateReviewGate(state)];
      break;
    case 'commit':
      errors = validateCommitConventions();
      break;
    case 'pr_finalization':
      errors = [...validateCIStatus(), ...validatePRDescription(), ...validateCommitConventions()];
      break;
    case 'finalization':
      errors = [...validateCIStatus(), ...validatePRDescription()];
      break;
    default:
      // No validation needed for this stage
      break;
  }

  if (errors.length > 0) {
    console.error(`[full-pipeline-validator] Gate blocked at stage: ${stage}`);
    errors.forEach((e) => console.error(`  - ${e}`));

    // Write validation result
    const validationFile = path.join(TASK_DIR, 'gate-validation.json');
    fs.mkdirSync(TASK_DIR, { recursive: true });
    fs.writeFileSync(validationFile, JSON.stringify({
      stage,
      passed: false,
      errors,
      timestamp: new Date().toISOString(),
    }, null, 2));

    // Don't exit with error - let the orchestrator read the validation file
    // and decide how to handle it
    process.exit(0);
  }

  // Write success
  const validationFile = path.join(TASK_DIR, 'gate-validation.json');
  fs.mkdirSync(TASK_DIR, { recursive: true });
  fs.writeFileSync(validationFile, JSON.stringify({
    stage,
    passed: true,
    errors: [],
    timestamp: new Date().toISOString(),
  }, null, 2));

  process.exit(0);
}

main();
